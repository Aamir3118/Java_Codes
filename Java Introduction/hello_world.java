class HelloWorld
{
    public static void main(String args[])
    {
        System.out.println("Hello World");
        int a=10;
        float f=10.2f;
        char c='\u0056';
        double d=10.5;
        long l=455500000L;
        System.out.println("int: "+a);
        System.out.println("float: "+f);
        System.out.println("char: "+c);
        System.out.println("double: "+d);
        System.out.println("long: "+l);
    }
}